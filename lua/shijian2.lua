-- 十进制转二进制
function Dec2bin(n)
  local t, t1, t2
  local tables = {""}
  t = tonumber(n)
  while math.floor(t / 2) >= 1 do
    t1 = math.fmod(t, 2)
    if t1 > 0 then
      if #tables > 0 then
        table.insert(tables, 1, 1)
      else
        tables[1] = 1
      end
    else
      if #tables > 0 then
        table.insert(tables, 1, 0)
      else
        tables[1] = 0
      end
    end
    t = math.floor(t / 2)
    if t == 1 then
      if #tables > 0 then
        table.insert(tables, 1, 1)
      else
        tables[1] = 1
      end
    end
  end
  return string.gsub(table.concat(tables), "^[0]+", "")
end

